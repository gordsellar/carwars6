    CW.findEngines = function (vehicleType, maxWeight, wheels, streamlined, acceleration, topSpeed, gas, electric, range, techLevel, noShortAllowed, maxEngineSpace, maxEngineWeight) {
        var i, j, entry, engine, engineAccel, engineTopSpeed, engineRange, usedMaxWeight, baseAccel;
        var working, result, maxResults = 2, weightIndex, found;
        var useMaxWeight = maxWeight * (noShortAllowed ? 1 : 0.95);
        if(techLevel === 'Classic') {
            electric = true;
            gas = false;
            if(!noShortAllowed) useMaxWeight = maxWeight * 0.9;
        }
        var gasResult = {
            lowestCost: [],
            leastSpace: null, // prefer lower cost, higher used max weight
            atMaxWeight: null // prefer lower cost
        }, electricResult = {
            lowestCost: [],
            leastSpace: null,
            atMaxWeight: null
        };
        var options = [];
        var oversize = false, semi = false;
        if (vehicleType === 'Car' || vehicleType === 'Cycle' || vehicleType === 'Trike') {
            for (entry in CW.gasEnginePerformance) {
                if (CW.gasEnginePerformance.hasOwnProperty(entry)) {
                    options.push(CW.gasEnginePerformance[entry]);
                }
            }
        }
        if (vehicleType === 'Car') {
            for (entry in CW.carPowerPlantPerformance) {
                if (CW.carPowerPlantPerformance.hasOwnProperty(entry)) {
                    options.push(CW.carPowerPlantPerformance[entry]);
                }
            }
        }
        if (vehicleType === 'Cycle' || vehicleType === 'Trike') {
            for (entry in CW.cyclePowerPlantPerformance) {
                if (CW.cyclePowerPlantPerformance.hasOwnProperty(entry)) {
                    options.push(CW.cyclePowerPlantPerformance[entry]);
                }
            }
        }
        if (vehicleType === 'SemiTractor' || vehicleType === 'Bus' || vehicleType === 'TenWheeler') {
            oversize = true;
            semi = vehicleType === 'SemiTractor';
            for (entry in CW.truckPowerPlantPerformance) {
                if (CW.truckPowerPlantPerformance.hasOwnProperty(entry)) {
                    options.push(CW.truckPowerPlantPerformance[entry]);
                }
            }
            for (entry in CW.truckEnginePerformance) {
                if (CW.truckEnginePerformance.hasOwnProperty(entry)) {
                    options.push(CW.truckEnginePerformance[entry]);
                }
            }
        }
        for (j = 0; j < options.length; j++) {
            engine = options[j];
            if (!gas && !engine.electric) continue;
            if (!electric && engine.electric) continue;
            if(techLevel === 'Classic') {
                if(engine.highTorqueMotors || engine.heavyDutyHighTorqueMotors || engine.platinumCatalysts || engine.superconductors) continue;
            }
            if (techLevel === 'CWC' && engine.electric && (engine.highTorqueMotors || engine.heavyDutyHighTorqueMotors)) continue;
            if (semi && engine.electric && (engine.name === 'Small' || engine.name === 'Medium')) continue;
            if (maxEngineWeight) {
                if (engine.electric) {
                    if (engine.weight > maxEngineWeight) continue;
                } else {
                    if (engine.weight + Math.ceil(range / engine.mpg - 0.0001) * 7 > maxEngineWeight) continue;
                }
            }
            if (maxEngineSpace) {
                if (engine.electric) {
                    if (engine.space > maxEngineSpace) continue;
                } else {
                    if (engine.space + Math.floor((Math.ceil(range / engine.mpg - 0.0001) + 4) / 10 + 0.0001) > maxEngineSpace) continue;
                }
            }
            if (oversize) {
                if (engine.powerFactors < useMaxWeight) continue;
                else engineAccel = 5;
            } else {
                if (engine.powerFactors < useMaxWeight / 3) continue;
                else if (engine.powerFactors < useMaxWeight / 2) engineAccel = 5;
                else if (engine.powerFactors < useMaxWeight) engineAccel = 10;
                else engineAccel = 15;
            }
            //            if(engineAccel > acceleration) continue;
            baseAccel = engineAccel;
            if (baseAccel === 5 && engine.heavyDutyHighTorqueMotors) continue;
            if (engine.electric) {
                if (engine.highTorqueMotors) engineAccel += 5;
                else if (engine.heavyDutyHighTorqueMotors) engineAccel = engineAccel * 2;
            } else {
                if (engine.turbocharger) engineAccel += 5;
                if (engine.supercharger) engineAccel += 5;
            }
            if (engineAccel < acceleration) continue;
            if (baseAccel === 5) {
                if (oversize) usedMaxWeight = Math.min(engine.powerFactors, maxWeight);
                else if (engine.powerFactors * 3 < maxWeight) usedMaxWeight = engine.powerFactors * 3;
                else usedMaxWeight = maxWeight;
            } else if (baseAccel === 10) {
                if (engine.powerFactors * 2 < maxWeight) usedMaxWeight = engine.powerFactors * 2;
                else usedMaxWeight = maxWeight;
            } else {
                if (engine.powerFactors < maxWeight) usedMaxWeight = engine.powerFactors;
                else usedMaxWeight = maxWeight;
            }
            if (oversize) {
                engineTopSpeed = 200 * engine.powerFactors / (engine.powerFactors + usedMaxWeight);
                engineRange = 200;
                working = engine.electric ? electricResult : gasResult;
            } else if (engine.electric) {
                engineTopSpeed = 360 * engine.powerFactors / (engine.powerFactors + usedMaxWeight);
                engineRange = 200;
                working = electricResult;
            } else {
                engineTopSpeed = 240 * engine.powerFactors / (engine.powerFactors + usedMaxWeight);
                engineRange = engine.mpg;
                working = gasResult;
            }
            if (streamlined) engineTopSpeed = engineTopSpeed * 1.1;
            if (engineTopSpeed < topSpeed) continue;
            if (working.lowestCost.length >= maxResults * 2 &&
                (engine.cost + wheels * engine.costPerTire) > working.lowestCost[working.lowestCost.length - 1].cost) continue;
            result = Object.create(engine);
            result.usedMaxWeight = usedMaxWeight;
            result.topSpeed = 2.5 * Math.floor(engineTopSpeed / 2.5 + 0.0001);
            result.acceleration = engineAccel;
            result.cost = engine.cost + (engine.costPerTire ? engine.costPerTire * wheels : 0);
            // Least Space
            if (working.leastSpace === null || result.space < working.leastSpace.space ||
                (result.space === working.leastSpace.space &&
                    ((result.cost < working.leastSpace.cost && result.usedMaxWeight === working.leastSpace.usedMaxWeight)
                        || result.usedMaxWeight > working.leastSpace.usedMaxWeight))) {
                working.leastSpace = result;
            }
            // At Max Weight
            if (result.usedMaxWeight === maxWeight) {
                if (working.atMaxWeight === null || result.cost < working.atMaxWeight.cost) {
                    working.atMaxWeight = result;
                }
            }
            // Lowest Cost
            for (i = 0; i < working.lowestCost.length; i++) {
                if (result.cost < working.lowestCost[i].cost) {
                    working.lowestCost.splice(i, 0, result);
                    result = null;
                    break;
                }
            }
            if (result !== null && working.lowestCost.length < maxResults * 2) working.lowestCost.push(result);
            if (working.lowestCost.length > maxResults * 2) working.lowestCost.pop();
        }
        while (electricResult.lowestCost.length + Math.min(maxResults, gasResult.lowestCost.length) > maxResults * 2) electricResult.lowestCost.pop();
        while (gasResult.lowestCost.length + Math.min(maxResults, electricResult.lowestCost.length) > maxResults * 2) gasResult.lowestCost.pop();
        if (gasResult.lowestCost.length > 1) {
            found = false;
            weightIndex = gasResult.lowestCost.length;
            if (gasResult.atMaxWeight !== null) {
                for (i = 0; i < gasResult.lowestCost.length; i++) {
                    if (gasResult.lowestCost[i].usedMaxWeight === maxWeight || gasResult.lowestCost[i] === gasResult.atMaxWeight) {
                        found = true;
                        break;
                    }
                }
                if (!found) {
                    weightIndex = gasResult.lowestCost.length - 1;
                    gasResult.lowestCost[weightIndex] = gasResult.atMaxWeight;
                }
            }
            if (weightIndex > 1 && gasResult.leastSpace !== null) {
                found = false;
                for (i = 0; i < gasResult.lowestCost.length; i++) {
                    if (gasResult.lowestCost[i] === gasResult.leastSpace) {
                        found = true;
                        break;
                    } else if(gasResult.lowestCost.length < 3 && gasResult.lowestCost[i].space <= gasResult.leastSpace.space) {
                        found = true;
                        break;
                    }
                }
                if (!found) {
                    gasResult.lowestCost[weightIndex - 1] = gasResult.leastSpace;
                }
            }
        }
        if (electricResult.lowestCost.length > 1) {
            found = false;
            weightIndex = electricResult.lowestCost.length;
            if (electricResult.atMaxWeight !== null) {
                for (i = 0; i < electricResult.lowestCost.length; i++) {
                    if (electricResult.lowestCost[i].usedMaxWeight === maxWeight || electricResult.lowestCost[i] === electricResult.atMaxWeight) {
                        found = true;
                        break;
                    }
                }
                if (!found) {
                    weightIndex = electricResult.lowestCost.length - 1;
                    electricResult.lowestCost[weightIndex] = electricResult.atMaxWeight;
                }
            }
            if (weightIndex > 1 && electricResult.leastSpace !== null) {
                found = false;
                for (i = 0; i < electricResult.lowestCost.length; i++) {
                    if (electricResult.lowestCost[i] === electricResult.leastSpace) {
                        found = true;
                        break;
                    } else if(electricResult.lowestCost.length < 3 && electricResult.lowestCost[i].space <= electricResult.leastSpace.space) {
                        found = true;
                        break;
                    }
                }
                if (!found) {
                    electricResult.lowestCost[weightIndex - 1] = electricResult.leastSpace;
                }
            }
        }
        result = gasResult.lowestCost.concat(electricResult.lowestCost);
        for (i = 0; i < result.length; i++) {
            CW.engineMods(result[i], result[i], result[i].electric ? 0 : Math.ceil(range / result[i].mpg - 0.0001), 2, 1);
        }
        return result;
    };

    CW.engineMods = function(source, result, gasTankGallons, tankCPG, tankWPG) {
        if (source.electric) {
            result.mods = (source.platinumCatalysts ? "PC, " : "") + (source.superconductors ? "SC, " : "") +
                (source.highTorqueMotors ? "HTM, " : "") + (source.heavyDutyHighTorqueMotors ? "HDHTM, " : "");
            if (result.mods.length > 0) result.mods = result.mods.substring(0, result.mods.length - 2);
        } else {
            result.gasTankSize = gasTankGallons;
            result.mods = (source.carburetor ? "Carb, " : "") + (source.multibarrelCarburetor ? "MB Carb, " : "") +
                (source.tubularHeaders ? "TH, " : "") + (source.blueprinted ? "BP, " : "") +
                (source.turbocharger ? "TC, " : "") + (source.supercharger ? "SC, " : "") +
                (result.gasTankSize + "g tank");
            result.cost += result.gasTankSize * (40+tankCPG);
            result.weight += result.gasTankSize * (6+tankWPG);
            result.space += Math.floor((result.gasTankSize + 4) / 10 + 0.0001);
        }
    };

